// Write a program to calculate CGPA using marks of three subjects (out of 100)?
public class CWH_ch_01_pr_02 {
    public static void main(String[] args) {
        float m1=90;
        float m2=60;
        float m3=45;
        float CGPA=(m1+m2+m3)/(30);
        System.out.println("The CGPA is : "+ CGPA);
    }
}
